#include "encrypt.h"

void xor_encrypt_decrypt(uint8_t *data, size_t size, uint64_t key)
{
    for (size_t i = 0; i < size; i++)
    {
        data[i] ^= ((key >> ((i % 8) * 8)) & 0xFF);
    }
}